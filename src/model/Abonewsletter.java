package model;

public class Abonewsletter {
	private int id;
	private String mail;
	private String firstName;
	private String lastName;
	private int userId;
	
	public Abonewsletter() {
		
	}
	
	public Abonewsletter(int id, String firstName, String lastName, int userId) {
		this.id = id;
		this.mail = mail;
		this.firstName = firstName;
		this.lastName = lastName;
		this.userId = userId;
	}
}
